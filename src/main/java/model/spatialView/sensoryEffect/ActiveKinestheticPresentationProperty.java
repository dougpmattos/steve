package model.spatialView.sensoryEffect;

public class ActiveKinestheticPresentationProperty extends EffectPresentationProperty {

    private float fx;
    private float fy;
    private float fz;

    private float tx;
    private float ty;
    private float tz;

    public float getFx() {
        return fx;
    }

    public void setFx(float fx) {
        this.fx = fx;
    }

    public float getFy() {
        return fy;
    }

    public void setFy(float fy) {
        this.fy = fy;
    }

    public float getFz() {
        return fz;
    }

    public void setFz(float fz) {
        this.fz = fz;
    }

    public float getTx() {
        return tx;
    }

    public void setTx(float tx) {
        this.tx = tx;
    }

    public float getTy() {
        return ty;
    }

    public void setTy(float ty) {
        this.ty = ty;
    }

    public float getTz() {
        return tz;
    }

    public void setTz(float tz) {
        this.tz = tz;
    }

}
